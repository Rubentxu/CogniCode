    // ═══════════════════════════════════════════════════════════════════════════

    fn run_rule_check(source: &str, language: &str, rule: &STYLE_002Rule) -> Vec<Issue> {
        use cognicode_core::infrastructure::parser::Language;
        use cognicode_core::domain::aggregates::call_graph::CallGraph;
        use std::path::Path;
        use tree_sitter::Parser as TsParser;

        let lang = match language {
            "javascript" => Language::JavaScript,
            "typescript" => Language::TypeScript,
            "python" => Language::Python,
            _ => Language::JavaScript,
        };

        let ts_language = lang.to_ts_language();
        let mut parser = TsParser::new();
        parser.set_language(&ts_language).unwrap();
        let tree = parser.parse(source, None).unwrap();
        let graph = CallGraph::new();
        let metrics = crate::rules::FileMetrics::new();
        let symbol_table = crate::rules::symbol_table::SymbolTableBuilder::new()
            .build(&tree, source);

        let ctx = RuleContext {
            tree: &tree,
            source,
            file_path: Path::new("test.js"),
            language: &lang,
            graph: &graph,
            metrics: &metrics,
            symbol_table: Some(&symbol_table),
        };

        rule.check(&ctx)
    }
