/// Minimum identifiers of each convention needed to confirm intentional mixing
const THRESHOLD_COUNT: usize = 3;

/// Markers that indicate a file is generated
const GENERATED_MARKERS: &[&str] = &[
    "// GENERATED",
    "# GENERATED",
    "/* GENERATED",
    "// This file is generated",
    "# This file is generated",
    "// Automatically generated",
    "# Automatically generated",
    "// Code generated by",
    "# Code generated by",
    "@generated",
];

/// Classification of naming conventions
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum NamingConvention {
    SnakeCase,
    CamelCase,
    PascalCase,
    ScreamingSnakeCase,
    Unknown,
}

impl NamingConvention {
    /// Classify a name into its naming convention
    fn classify(name: &str) -> Self {
        if name.is_empty() {
            return NamingConvention::Unknown;
        }

        // SCREAMING_SNAKE_CASE: all uppercase with underscores, numbers allowed
        if name.chars().all(|c| c.is_uppercase() || c == '_' || c.is_numeric())
            && name.contains('_')
            && !name.chars().take(1).all(|c| c.is_numeric())
        {
            return NamingConvention::ScreamingSnakeCase;
        }

        let has_underscore = name.contains('_');
        let has_uppercase = name.chars().any(|c| c.is_uppercase());

        // Check if it starts with uppercase (PascalCase)
        let starts_uppercase = name.chars().next().map(|c| c.is_uppercase()).unwrap_or(false);

        if starts_uppercase {
            // Could be PascalCase or acronym
            if has_underscore {
                // Mix of PascalCase and underscore suggests inconsistent style
                NamingConvention::Unknown
            } else if has_uppercase {
                // It's PascalCase if it has uppercase in the middle
                if name.chars().skip(1).any(|c| c.is_uppercase()) {
                    NamingConvention::PascalCase
                } else {
                    // Single uppercase letter at start might be an abbreviation
                    NamingConvention::PascalCase
                }
            } else {
                NamingConvention::PascalCase
            }
        } else {
            // Starts with lowercase
            if has_underscore && has_uppercase {
                // Mix of underscores and uppercase is inconsistent
                NamingConvention::Unknown
            } else if has_underscore {
                NamingConvention::SnakeCase
            } else if has_uppercase {
                // camelCase has uppercase but no underscores, and doesn't start with uppercase
                NamingConvention::CamelCase
            } else {
                // All lowercase with no underscore - could be a simple name
                NamingConvention::SnakeCase
            }
        }
    }

    fn as_str(&self) -> &'static str {
        match self {
            NamingConvention::SnakeCase => "snake_case",
            NamingConvention::CamelCase => "camelCase",
            NamingConvention::PascalCase => "PascalCase",
            NamingConvention::ScreamingSnakeCase => "SCREAMING_SNAKE_CASE",
            NamingConvention::Unknown => "unknown",
        }
    }
}

/// Check if the file appears to be generated
fn is_generated_file(source: &str) -> bool {
    for line in source.lines().take(10) {
        let trimmed = line.trim();
        for marker in GENERATED_MARKERS {
            if trimmed.contains(marker) {
                return true;
            }
        }
    }
    false
}

/// Collect all identifiers from the AST using tree-sitter queries
fn collect_identifiers(ctx: &RuleContext) -> Vec<(String, NamingConvention, usize)> {
    let mut identifiers = Vec::new();

    // Language-specific identifier node kinds
    let identifier_kinds = match ctx.language.name() {
        "JavaScript" | "TypeScript" => vec![
            "identifier",
            "property_identifier",
            "variable_declarator",
        ],
        "Python" => vec![
            "identifier",
            "attribute",
        ],
        _ => vec!["identifier"],
    };

    // Use a generic query to find all identifier-like nodes
    let query_pattern = format!(
        "(identifier) @id"
    );

    if let Ok(query) = tree_sitter::Query::new(&ctx.language.to_ts_language(), &query_pattern) {
        let mut cursor = tree_sitter::QueryCursor::new();
        let mut matches = cursor.matches(&query, ctx.tree.root_node(), ctx.source.as_bytes());

        while let Some(m) = matches.next() {
            for capture in m.captures {
                let node = capture.node;
                if let Ok(name) = node.utf8_text(ctx.source.as_bytes()) {
                    let line = node.start_position().row + 1;
                    let convention = NamingConvention::classify(name);

                    // Skip single character names (likely loop counters)
                    if name.len() <= 1 {
                        continue;
                    }

                    // Skip names that are all lowercase but have no underscore
                    // (they might be regular words, not identifiers)
                    if convention == NamingConvention::SnakeCase
                        && !name.contains('_')
                        && name.chars().all(|c| c.is_lowercase())
                        && name.len() < 3
                    {
                        continue;
                    }

                    identifiers.push((name.to_string(), convention, line));
                }
            }
        }
    }

    identifiers
}

declare_rule! {
    id: "STYLE_002"
    name: "Inconsistent naming conventions within a file"
    severity: Minor
    category: CodeSmell
    language: "javascript,typescript,python"
    params: {
        threshold: usize = 3
    }

    explanation: "When a file contains a mixture of naming conventions (snake_case and camelCase), it violates consistency principles and increases cognitive load. All identifiers within a file should follow the same naming convention. The threshold of 3+ identifiers of each convention is required to confirm mixing is intentional, not accidental."
    clean_code: Focused,
    impacts: [Maintainability: Low],
    auto_test: true

    agent_semantics: {
        summary: "Mixed naming conventions detected - all identifiers should follow consistent style",
        fix_playbook: "1. Identify which naming convention is appropriate for the language\n2. List all identifiers that don't follow the convention\n3. Use IDE refactoring to rename identifiers consistently\n4. Ensure snake_case Python code doesn't mix with camelCase\n5. For JavaScript/TypeScript: ensure functions/variables use camelCase",
        review_questions: [
            "Is this file imported from external source?",
            "Is this code in transition between conventions?",
            "Which convention should be used for this language?",
            "Are there at least 3 instances of each convention to confirm mixing?"
        ],
        semantic_chunks: [
            "Inconsistent naming conventions increase cognitive load",
            "Python should use snake_case; JavaScript/TypeScript should use camelCase",
            "Mixed conventions in same file suggest incomplete refactoring or external imports",
            "Fix: Choose appropriate convention and refactor all identifiers to match"
        ],
        safe_autofix: false,
        autofix_guidance: "Cannot safely autofix without context - requires choosing correct convention and ensuring external APIs are not broken"
    }

    check: => {
        let mut issues = Vec::new();

        // Skip generated files
        if is_generated_file(ctx.source) {
            return issues;
        }

        // Collect all identifiers
        let identifiers = collect_identifiers(ctx);

        // Filter out SCREAMING_SNAKE_CASE constants (valid exception)
        let filtered: Vec<_> = identifiers.into_iter()
            .filter(|(name, convention, _)| {
                // Skip SCREAMING_SNAKE_CASE constants
                if *convention == NamingConvention::ScreamingSnakeCase {
                    return false;
                }
                // Note: We don't filter "external" calls here because we can't reliably
                // distinguish library calls from user-defined functions without scope info.
                // The inconsistency detection relies on threshold counting instead.
                true
            })
            .collect();

        if filtered.len() < self.threshold {
            // Not enough identifiers to determine inconsistency
            return issues;
        }

        // Count identifiers by convention
        let mut convention_counts: std::collections::HashMap<NamingConvention, Vec<(String, usize)>> =
            std::collections::HashMap::new();

        for (name, convention, line) in &filtered {
            if *convention != NamingConvention::Unknown {
                convention_counts.entry(*convention)
                    .or_default()
                    .push((name.clone(), *line));
            }
        }

        // Need at least 2 different conventions to have inconsistency
        let conventions: Vec<_> = convention_counts.keys().collect();
        if conventions.len() < 2 {
            return issues;
        }

        // Find the dominant convention (most identifiers)
        let mut convention_vec: Vec<_> = convention_counts.iter().collect();
        convention_vec.sort_by(|a, b| b.1.len().cmp(&a.1.len()));

        let threshold = self.threshold;
        let dominant = convention_vec[0];
        let dominant_convention = *dominant.0;
        let dominant_count = dominant.1.len();

        // If dominant convention doesn't have threshold+, we can't determine inconsistency
        if dominant_count < threshold {
            return issues;
        }

        // Report issues for each non-dominant convention's identifiers
        for (conv, items) in &convention_counts {
            if *conv == dominant_convention {
                continue;
            }

            for (name, line) in items {
                issues.push(Issue::new(
                    "STYLE_002",
                    format!(
                        "Inconsistent naming: '{}' follows {} but other items use {}",
                        name,
                        conv.as_str(),
                        dominant_convention.as_str()
                    ),
                    Severity::Minor,
                    Category::CodeSmell,
                    ctx.file_path,
                    *line,
                ).with_remediation(Remediation::moderate(
                    format!(
                        "Use consistent naming convention. Consider changing '{}' to match {} style.",
                        name,
                        dominant_convention.as_str()
                    )
                )));
            }
        }

        issues
    }
}

