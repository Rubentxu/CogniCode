//! STYLE_002 — Inconsistent naming conventions within a file
//!
//! Detects when a file contains a mixture of naming conventions (snake_case and camelCase)
//! for similar items (functions, variables, etc.).
//!
//! ## Naming Conventions
//! - `snake_case`: lowercase with underscores (e.g., `my_variable`)
//! - `camelCase`: lowercase start, uppercase words (e.g., `myVariable`)
//! - `PascalCase`: uppercase start (e.g., `MyStruct`)
//! - `SCREAMING_SNAKE_CASE`: uppercase with underscores (e.g., `MY_CONSTANT`)
//!
//! ## False Positives
//! - External/library API calls (cannot control their naming)
//! - Generated code (build outputs, protobuf outputs)
//! - Constants written in UPPER_SNAKE_CASE (valid exception)
//! - Files with only 1-2 identifiers (insufficient sample)
//! - Generated code markers like "// GENERATED CODE"
use crate::{Severity, Category, Issue, Remediation, Rule, RuleContext, RuleEntry};
use crate::rules::{CleanCodeAttribute, SoftwareQuality, SoftwareQualityImpact, ImpactSeverity};
